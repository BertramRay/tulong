#!/usr/bin/env python3
class ShapeNode:
    def __init__(self, width=0, height=0, x=0, y=0):
        self.shape = ""
        self.width = width
        self.height = height
        self.x = x
        self.y = y
        self.borderRadius = None
        self.borderWidth = None
        self.borderColor = None
        self.backgroundColor = None
